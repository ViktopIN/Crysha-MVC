import UIKit.UIColor

extension UIColor {
    static var customBlue: UIColor {
        return UIColor(hue: 215/360, saturation: 0.69, brightness: 0.84, alpha: 1)
    }
    static var customGreen: UIColor {
        return UIColor(hue: 96/360, saturation: 0.59, brightness: 0.73, alpha: 1)
    }

}
